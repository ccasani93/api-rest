package com.idat.idatapirest.dto;

public class ItemResponseDTO {

	private Integer iditem;
	private String nombreitem;
	private Integer cantidaditem;
	private Double totalitem;
	/**
	 * @return the iditem
	 */
	public Integer getIditem() {
		return iditem;
	}
	/**
	 * @param iditem the iditem to set
	 */
	public void setIditem(Integer iditem) {
		this.iditem = iditem;
	}
	/**
	 * @return the nombreitem
	 */
	public String getNombreitem() {
		return nombreitem;
	}
	/**
	 * @param nombreitem the nombreitem to set
	 */
	public void setNombreitem(String nombreitem) {
		this.nombreitem = nombreitem;
	}
	/**
	 * @return the cantidaditem
	 */
	public Integer getCantidaditem() {
		return cantidaditem;
	}
	/**
	 * @param cantidaditem the cantidaditem to set
	 */
	public void setCantidaditem(Integer cantidaditem) {
		this.cantidaditem = cantidaditem;
	}
	/**
	 * @return the totalitem
	 */
	public Double getTotalitem() {
		return totalitem;
	}
	/**
	 * @param totalitem the totalitem to set
	 */
	public void setTotalitem(Double totalitem) {
		this.totalitem = totalitem;
	}
	
	
}
